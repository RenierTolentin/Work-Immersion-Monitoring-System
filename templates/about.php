                <h2>Project Context</h2>
                <p class="wrap">The use of technology is become a big part of our daily lives from our daily routine,
                banking and financing, security and the most of all the attendance tracking it plays a significant
                role in every companies but in many cases this was being set aside many companies and small businesses
                are still using old systematic way of performing attendance tracking. Files and its records can be
                easily altered as it was open for every change’s and can be accessed physically.
                The researchers aimed to determine the Work Immersion Monitoring System for OJT’s using RFID. Automation is very
                important, especially when any disaster occurs. One of the things that RFID can help is to make a
                contactless attendance system that will help the company in terms of; hygiene, by not touching any
                materials that others can touch to and second is automation process which smoothen the flow of the
                data and also the people. Most of the companies/institution or even small businesses still use papers and
                other tangible materials to preserve the information and other data that may result for it to get damaged
